import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
import { fileURLToPath } from 'node:url';
const root=path.resolve(path.dirname(fileURLToPath(import.meta.url)),'..');
const results=[]; const add=(name,ok,detail='')=>{results.push({name,ok:Boolean(ok),detail}); if(!ok) process.exitCode=1;};
const context={ console, Math, Date, setTimeout, clearTimeout };
context.globalThis=context; context.window=context;
for(const rel of ['data/vehicle-data.js','src/core/vehicle-physics.js','data/regulation-data.js','src/core/regulation-engine.js','data/strategy-data.js','src/core/race-strategy-ai.js']) vm.runInNewContext(fs.readFileSync(path.join(root,rel),'utf8'),context,{filename:rel});
const vehicle=context.F1M_CORE.vehiclePhysics.createVehiclePhysics({data:context.F1M_VEHICLE_DATA});
const ai=context.F1M_CORE.strategyAI.createRaceStrategyAI({data:context.F1M_STRATEGY_DATA,vehiclePhysics:vehicle});
add('strategy:factory', typeof context.F1M_CORE.strategyAI.createRaceStrategyAI === 'function');
const audit=ai.audit();
add('strategy:audit-score', audit.score >= 92, String(audit.score));
for(const id of ['initial-plan','undercut-overcut','traffic','attack-defense','human-error','pit-crew','double-stacking','unsafe-release','safety-car','vsc','red-flag','restart','overtake-resolution']) add(`strategy:${id}`, audit.checks.some(item=>item.id===id && item.ok));
const race={ laps:22, speed:3, tick:15, weather:'dry', safetyCar:0, redFlag:0, restartTimer:0, strategyState:{pitLaneBusy:0}, raceLog:[], entries:[] };
const team={id:'t1',name:'Equipe A',car:{pitStop:66,reliability:62,tyreWear:70}};
const team2={id:'t2',name:'Equipe B',car:{pitStop:54,reliability:58,tyreWear:58}};
const d1={short:'AAA',aggression:82,consistency:72,racecraft:80};
const d2={short:'BBB',aggression:62,consistency:78,racecraft:66};
const plan=ai.initialPlan(d1,team,{track:{laps:22,weather:'dry'},weather:'dry'},4);
add('strategy:plan-has-pit-window', Array.isArray(plan.pitWindow) && plan.pitWindow.length===2 && plan.startCompound, JSON.stringify(plan));
const e1={driver:d1,team,pos:2,distance:2.10,progress:2.10,lap:8,compound:plan.startCompound,tyre:51,fuel:72,condition:92,pits:0,car:team.car,strategy:plan,pace:'normal'};
const e2={driver:d2,team:team2,pos:1,distance:2.11,progress:2.11,lap:8,compound:'medium',tyre:43,fuel:71,condition:90,pits:0,car:team2.car,strategy:ai.initialPlan(d2,team2,{track:{laps:22}},1),pace:'normal'};
race.entries=[e2,e1];
const pressure=ai.trafficPressure(e1,race);
add('strategy:traffic-pressure', pressure.traffic && pressure.drsRange, JSON.stringify(pressure));
const decision=ai.driverDecision(e1,race,{dt:0.5,isPlayer:false,context:{neutralized:false}});
add('strategy:decision-pace', ['attack','normal','save'].includes(decision.pace), decision.pace);
add('strategy:decision-intent', Boolean(decision.intent), decision.intent);
const before=e1.progress;
const pit=ai.executePitStop(e1,race,{vehiclePhysics:vehicle});
add('strategy:pit-service', e1.pits===1 && e1.progress < before && e1.tyre===100 && pit.loss>0, JSON.stringify(pit));
ai.deployNeutralization(race,'sc');
add('strategy:safety-car-deploys', race.safetyCar>0 && race.strategyState.safetyCarDeployments>=1, String(race.safetyCar));
ai.deployNeutralization(race,'red');
add('strategy:red-flag-deploys', race.redFlag>0 && race.strategyState.redFlags>=1, String(race.redFlag));
race.redFlag=0; race.safetyCar=0; e1.progress=2.109; e2.progress=2.110; e1.drs=true; e1.aiIntent='ataque';
for(let i=0;i<40;i++) ai.resolveOvertakes(race,{dt:1,speed:4,vehiclePhysics:vehicle});
add('strategy:overtake-safe', race.entries.length===2 && Number.isFinite(e1.progress) && Number.isFinite(e2.progress), `${e1.progress}/${e2.progress}`);
const report={build:JSON.parse(fs.readFileSync(path.join(root,'BUILD_INFO.json'),'utf8')).build_code,generatedAt:new Date().toISOString(),passed:results.filter(r=>r.ok).length,failed:results.filter(r=>!r.ok).length,results,audit};
fs.mkdirSync(path.join(root,'test-results'),{recursive:true});
fs.writeFileSync(path.join(root,'test-results/strategy-ai-audit.json'),JSON.stringify(report,null,2)+'\n');
console.log(results.map(r=>`${r.ok?'PASS':'FAIL'} ${r.name}${r.detail?' — '+r.detail:''}`).join('\n'));
console.log(`TOTAL: ${report.passed} passed, ${report.failed} failed`);
