import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
import { fileURLToPath } from 'node:url';
const root=path.resolve(path.dirname(fileURLToPath(import.meta.url)),'..');
const results=[]; const add=(name,ok,detail='')=>{results.push({name,ok:Boolean(ok),detail:String(detail||'')}); if(!ok) process.exitCode=1;};
const build=JSON.parse(fs.readFileSync(path.join(root,'BUILD_INFO.json'),'utf8'));
const sandbox={console,Date,JSON}; sandbox.globalThis=sandbox; sandbox.window=sandbox;
vm.createContext(sandbox);
for(const rel of ['data/sporting-data.js','src/core/sporting-database.js','data/build-info.js','data/game-data.js']) vm.runInContext(fs.readFileSync(path.join(root,rel),'utf8'),sandbox,{filename:rel});
const db=sandbox.F1M_CORE.sporting.createDatabase({data:sandbox.F1M_SPORTING_DATA, legacy:sandbox.F1M_DATA});
const audit=db.audit();
const f1=db.calendarForSeries('F1'); const f2=db.calendarForSeries('F2');
add('build:phase-at-least-10', Number(build.phase)>=10, String(build.phase));
add('f1:calendar-22', f1.length===22, String(f1.length));
add('f1:first-australia', f1[0]?.id==='australia' && f1[0]?.dateStart==='2026-03-06', JSON.stringify(f1[0]));
add('f1:next-austria-after-barcelona', f1[7]?.id==='austria' && f1[7]?.status==='next', JSON.stringify(f1[7]));
add('f1:no-bahrain-saudi-in-current-calendar', !f1.some(r=>['bahrein','bahrain','saudi'].includes(r.id)), f1.map(r=>r.id).slice(0,5).join(','));
add('f2:calendar-14', f2.length===14, String(f2.length));
add('f2:first-melbourne', f2[0]?.id==='melbourne' && f2[0]?.weekendFormat==='SPRINT_AND_FEATURE', JSON.stringify(f2[0]));
add('f2:final-yas-marina', f2[13]?.id==='yas_marina', JSON.stringify(f2[13]));
const drivers=sandbox.F1M_SPORTING_DATA.f2DriversOfficial || [];
add('f2:drivers-22', drivers.length===22, String(drivers.length));
add('f2:teams-11', new Set(drivers.map(d=>d.team)).size===11, String(new Set(drivers.map(d=>d.team)).size));
add('f2:emerson-fittipaldi-aix', drivers.some(d=>d.name==='Emerson Fittipaldi' && d.team==='aix'), 'AIX Racing');
add('f2:no-enzo-jr', !drivers.some(d=>/Enzo Fittipaldi Jr\.?/i.test(d.name)), drivers.map(d=>d.name).join(', '));
add('legacy:data-current-calendar-f2', sandbox.F1M_DATA.calendar2026.length===14, String(sandbox.F1M_DATA.calendar2026.length));
add('legacy:data-f2-drivers-official', sandbox.F1M_DATA.f2Drivers.some(d=>d.name==='Emerson Fittipaldi') && !sandbox.F1M_DATA.f2Drivers.some(d=>/Enzo Fittipaldi Jr\.?/i.test(d.name)), 'F2 roster');
add('rules:f2-sprint-feature', Array.isArray(db.rulesForSeries('F2').sprintPoints) && Array.isArray(db.rulesForSeries('F2').featurePoints), 'F2 rules');
add('legal:commercial-review', sandbox.F1M_SPORTING_DATA.legalMode?.commercialStatus==='requires-licensing-review', sandbox.F1M_SPORTING_DATA.legalMode?.commercialStatus);
add('audit:internal-pass', audit.failed===0, `${audit.passed}/${audit.failed}`);
const report={build:build.build_code,generatedAt:new Date().toISOString(),passed:results.filter(r=>r.ok).length,failed:results.filter(r=>!r.ok).length,results,audit};
fs.writeFileSync(path.join(root,'test-results/sporting-data-audit.json'),JSON.stringify(report,null,2)+'\n');
console.log(results.map(r=>`${r.ok?'PASS':'FAIL'} ${r.name}${r.detail?' — '+r.detail:''}`).join('\n'));
console.log(`TOTAL: ${report.passed} passed, ${report.failed} failed`);
