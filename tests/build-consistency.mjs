import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
import { fileURLToPath } from 'node:url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const read = rel => fs.readFileSync(path.join(root,rel),'utf8');
const json = rel => JSON.parse(read(rel));
const results=[];
const add=(name,ok,detail='')=>{ results.push({name,ok:Boolean(ok),detail}); if(!ok) process.exitCode=1; };
const source=json('config/build-source.json');
const build=json('BUILD_INFO.json');
const data=json('data/build.json');
const pkg=json('package.json');
const packageLock=json('package-lock.json');
const manifest=json('manifest.webmanifest');
const assets=json('assets/ASSET_MANIFEST.json');
const shell=json('data/app-shell.json');
const index=read('index.html');
const sandbox={globalThis:{}}; vm.createContext(sandbox); vm.runInContext(read('data/build-info.js'),sandbox);
const runtime=sandbox.globalThis.F1M_BUILD;
for(const [label,value] of [['source',source.version],['root',build.version],['data',data.version],['runtime',runtime.version],['package',pkg.version],['manifest',manifest.version],['assets',assets.build_version]]) add(`version:${label}`,value===source.version,String(value));
add('version:package-lock',packageLock.version===source.version && packageLock.packages?.['']?.version===source.version,`${packageLock.version}/${packageLock.packages?.['']?.version}`);
for(const [label,value] of [['source',source.build_code],['root',build.build_code],['data',data.build_code],['runtime',runtime.build_code],['assets',assets.build_code],['shell',shell.build_code]]) add(`code:${label}`,value===source.build_code,String(value));
add('html:build-code',index.includes(`name="f1m-build-code" content="${source.build_code}"`));
add('html:version',index.includes(`name="f1m-build-version" content="${source.version}"`));
add('schema:save',Number(build.save_schema)===Number(source.phase),String(build.save_schema));
add('schema:asset-manifest',Number(assets.manifest_version)===Number(build.assets_manifest),String(assets.manifest_version));
add('schema:app-shell',Number(shell.schema)===Number(build.app_shell_schema),String(shell.schema));
for(const file of shell.files.filter(file=>file!=='./')) add(`shell:${file}`,fs.existsSync(path.join(root,file.replace(/^\.\//,''))),file);
fs.writeFileSync(path.join(root,'test-results/build-consistency.json'),JSON.stringify({build:source.build_code,generatedAt:new Date().toISOString(),results},null,2)+'\n');
console.log(results.map(r=>`${r.ok?'PASS':'FAIL'} ${r.name}${r.detail?' — '+r.detail:''}`).join('\n'));
