import fs from 'node:fs';
import path from 'node:path';
import vm from 'node:vm';
import { fileURLToPath } from 'node:url';
const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const results=[]; const add=(name,ok,detail='')=>{results.push({name,ok:Boolean(ok),detail}); if(!ok) process.exitCode=1;};
const read=rel=>fs.readFileSync(path.join(root,rel),'utf8');
for(const rel of ['data/living-career-data.js','src/core/living-career-system.js']){
  try{ new vm.Script(read(rel),{filename:rel}); add(`syntax:${rel}`,true); } catch(error){ add(`syntax:${rel}`,false,error.message); }
}
const sandbox = { globalThis:{}, window:{} };
sandbox.globalThis = sandbox; sandbox.window = sandbox;
vm.createContext(sandbox);
vm.runInContext(read('data/living-career-data.js'), sandbox);
vm.runInContext(read('src/core/living-career-system.js'), sandbox);
const api = sandbox.F1M_CORE?.livingCareer?.createLivingCareerSystem?.({ data:sandbox.F1M_LIVING_CAREER_DATA });
const state = { money:2500000, reputation:48, pressReputation:52, teamMorale:60, boardPressure:45, seasonYear:2026 };
api?.initializeState?.(state, { buildCode:'TEST' });
const audit = api?.audit?.({ state });
const board = api?.evaluateBoard?.(state);
const reg = api?.forecastRegulationImpact?.(state);
const projection = api?.projectMultiSeason?.(state);
const sponsors = api?.sponsorPortfolio?.(state);
const actions = api?.recommendActions?.(state);
add('living:data-loaded', Number(sandbox.F1M_LIVING_CAREER_DATA?.schema) >= 1, 'schema '+sandbox.F1M_LIVING_CAREER_DATA?.schema);
add('living:core-api', Boolean(api?.initializeState && api?.evaluateBoard && api?.projectMultiSeason && api?.monthlyTick), 'api completa');
add('living:audit-score', Number(audit?.score || 0) >= 100, `${audit?.score || 0}/100`);
add('living:departments', (sandbox.F1M_LIVING_CAREER_DATA?.departments || []).length >= 6, String((sandbox.F1M_LIVING_CAREER_DATA?.departments || []).length));
add('living:facilities', (sandbox.F1M_LIVING_CAREER_DATA?.facilities || []).length >= 8, String((sandbox.F1M_LIVING_CAREER_DATA?.facilities || []).length));
add('living:staff-archetypes', (sandbox.F1M_LIVING_CAREER_DATA?.staffArchetypes || []).length >= 6, String((sandbox.F1M_LIVING_CAREER_DATA?.staffArchetypes || []).length));
add('living:sponsor-tiers', (sandbox.F1M_LIVING_CAREER_DATA?.sponsorTiers || []).length >= 4, String((sandbox.F1M_LIVING_CAREER_DATA?.sponsorTiers || []).length));
add('living:academy-pipeline', (sandbox.F1M_LIVING_CAREER_DATA?.academyPipeline || []).length >= 4, String((sandbox.F1M_LIVING_CAREER_DATA?.academyPipeline || []).length));
add('living:board-politics', Object.keys(board?.factions || {}).length >= 4, JSON.stringify(board?.factions || {}));
add('living:regulation-watch', (reg || []).length >= 4 && reg.every(r=>Number.isFinite(r.risk)), String((reg || []).length));
add('living:multi-season', (projection || []).length === 3 && projection.every(p=>p.season && p.expectedIdentity), JSON.stringify(projection || []));
add('living:recommendations', (actions || []).length >= 1, String((actions || []).length));
add('living:sponsor-portfolio', (sponsors || []).length >= 4 && sponsors.some(s=>s.available), String((sponsors || []).length));
const index = read('index.html'); const script = read('script.js'); const css = read('style.css'); const pkg = JSON.parse(read('package.json'));
add('index:scripts', index.includes('data/living-career-data.js') && index.includes('src/core/living-career-system.js'), 'scripts F16');
add('script:system-card', script.includes('livingCareerMiniHTML') && script.includes('runLivingCareerAudit') && script.includes('runLivingCareerReview'), 'central sistema');
add('script:state-init', script.includes('livingCareer?.initializeState') && script.includes('state.livingCareer'), 'runtime state');
add('script:actions', script.includes('runLivingCareerAudit(){ runLivingCareerAudit(); }') && script.includes('runLivingCareerReview(){ runLivingCareerReview(); }'), 'actions');
add('css:living-career', css.includes('F16') && css.includes('living-career-card') && css.includes('living-metric-grid'), 'css');
add('package:script', Boolean(pkg.scripts?.['test:living-career']), 'test:living-career');
const summary={build:JSON.parse(read('BUILD_INFO.json')).build_code, generatedAt:new Date().toISOString(), passed:results.filter(r=>r.ok).length, failed:results.filter(r=>!r.ok).length, results};
fs.mkdirSync(path.join(root,'test-results'),{recursive:true});
fs.writeFileSync(path.join(root,'test-results/living-career-audit.json'),JSON.stringify(summary,null,2)+'\n');
console.log(results.map(r=>`${r.ok?'PASS':'FAIL'} ${r.name}${r.detail?' — '+r.detail:''}`).join('\n'));
console.log(`TOTAL: ${summary.passed} passed, ${summary.failed} failed`);
